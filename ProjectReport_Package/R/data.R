#' Global Development Indicators Dataset
#'
#' This dataset contains key economic and social indicators from the World Bank,
#' compiled for analysis of global growth and development patterns.
#'
#' @format A data frame with `n` rows and `p` columns:
#' \describe{
#'   \item{Country}{Country name}
#'   \item{Country.Code}{ISO country code}
#'   \item{year}{Year of observation}
#'   \item{CM.MKT.LCAP.GD.ZS}{Market capitalization of listed domestic companies (% of GDP)}
#'   \item{EG.IMP.CONS.ZS}{Energy imports, net (% of energy use)}
#'   \item{EG.USE.ELEC.KH.PC}{Electric power consumption (kWh per capita)}
#'   \item{EN.URB.LCTY.UR.ZS}{Population in the largest city (% of urban population)}
#'   \item{FP.CPI.TOTL.ZG}{Inflation, consumer prices (annual %)}
#'   \item{FR.INR.LNDP}{Interest rate spread (lending rate minus deposit rate, %)}
#'   \item{FS.AST.PRVT.GD.ZS}{Domestic credit to private sector (% of GDP)}
#'   \item{GB.XPD.RSDV.GD.ZS}{Research and development expenditure (% of GDP)}
#'   \item{GC.NLD.TOTL.GD.ZS}{Net lending (+) / net borrowing (-) (% of GDP)}
#'   \item{GC.TAX.TOTL.GD.ZS}{Tax revenue (% of GDP)}
#'   \item{GC.XPN.TOTL.GD.ZS}{Expense (% of GDP)}
#'   \item{IC.BUS.DISC.XQ}{Business extent of disclosure index (0=less disclosure to 10=more disclosure)}
#'   \item{IC.CRD.INFO.XQ}{Depth of credit information index (0=low to 8=high)}
#'   \item{IC.GOV.DURS.ZS}{Time spent dealing with government regulations (% of senior management time)}
#'   \item{IC.LGL.CRED.XQ}{Strength of legal rights index (0=weak to 12=strong)}
#'   \item{IC.REG.DURS.FE}{Time to start a business, female (days)}
#'   \item{IC.REG.DURS.MA}{Time to start a business, male (days)}
#'   \item{IC.TAX.TOTL.CP.ZS}{Total tax and contribution rate (% of profit)}
#'   \item{IT.CEL.SETS.P2}{Mobile cellular subscriptions (per 100 people)}
#'   \item{IT.MLT.MAIN.P2}{Fixed telephone subscriptions (per 100 people)}
#'   \item{IT.NET.BBND.P2}{Fixed broadband subscriptions (per 100 people)}
#'   \item{LP.LPI.OVRL.XQ}{Logistics performance index: Overall (1=low to 5=high)}
#'   \item{MS.MIL.XPND.GD.ZS}{Military expenditure (% of GDP)}
#'   \item{NY.GDP.MKTP.KD.ZG}{GDP growth (annual %)}
#'   \item{SE.PRM.TCAQ.ZS}{Trained teachers in primary education (% of total teachers)}
#'   \item{SE.XPD.TOTL.GD.ZS}{Government expenditure on education, total (% of GDP)}
#'   \item{SI.POV.GINI}{GINI index (World Bank estimate)}
#'   \item{SI.POV.URHC}{Urban poverty headcount ratio at national poverty lines (% of urban population)}
#'   \item{SL.TLF.CACT.FE.ZS}{Labor force participation rate, female (% of female population 15+)}
#'   \item{SL.TLF.CACT.MA.ZS}{Labor force participation rate, male (% of male population 15+)}
#'   \item{SL.UEM.TOTL.FE.ZS}{Unemployment, female (% of female labor force)}
#'   \item{SL.UEM.TOTL.MA.ZS}{Unemployment, male (% of male labor force)}
#'   \item{SP.URB.TOTL.IN.ZS}{Urban population (% of total population)}
#'   \item{TX.VAL.TECH.MF.ZS}{High-technology exports (% of manufactured exports)}
#' }
